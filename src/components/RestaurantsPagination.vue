<template>
  <nav aria-label="Page navigation example">
    <ul class="pagination">
      <!-- 前一頁 previousPage -->
      <li :class="['page-item', { disabled: currentPage === 1 }]" v-show="previousPage">
        <router-link class="page-link" :to="{name: 'restaurants', query: { categoryId, previousPage }}" aria-label="Previous">
          <span aria-hidden="true">&laquo;</span>
        </router-link>
      </li>

      <li v-for="page in totalPage" :key="page" :class="['page-item', { active: currentPage === page }]">
        <router-link class="page-link" :to="{name: 'restaurants', query: { categoryId, page }}">
          {{ page }}
        </router-link>
      </li>

      <!-- 後一頁 nextPage -->
      <li :class="['page-item', { disabled: currentPage === totalPage.length }]" v-show="nextPage">
        <router-link class="page-link" :to="{name: 'restaurants', query: { categoryId, nextPage }}" aria-label="Next">
          <span aria-hidden="true">&raquo;</span>
        </router-link>
      </li>
    </ul>
  </nav>
</template>

<script>
export default {
  props: {
    categoryId: {
      type: [String, Number],
      default: ''
    },
    currentPage: {
      type: Number,
      default: 1
    },
    totalPage: {
      type: Array,
      required: true
    },
    previousPage: {
      type: Number,
      required: true
    },
    nextPage: {
      type: Number,
      required: true
    }
  }
}
</script>